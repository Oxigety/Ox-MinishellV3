#include "libft.h"

void	*ft_memset( void *p, int v, size_t c)
{
	size_t	i;
	char	*set;

	i = 0;
	set = (char *)p;
	while (i < c)
	{
		set[i] = v;
		i++;
	}
	return (set);
}
