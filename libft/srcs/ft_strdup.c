#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*str;
	int		i;

	len = ft_strlen(s);
	str = malloc(sizeof(char) * len + 1);
	if (!str || !s)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
